def run_layer(node_id: str, layer: str, answers: list[str]) -> bool:
    """跑通一层,返回是否成功(can_advance=True)"""
    print(f"\n  📖 [{layer}] 层开始...")
    all_answers = list(answers) + list(GENERIC_FOLLOWUPS)

    for i, ans in enumerate(all_answers):
        label = f"核心第{i+1}轮" if i < len(answers) else f"补充第{i-len(answers)+1}轮"
        print(f"  💬 [{label}] {ans[:70]}...")
        try:
            resp = call_api("POST", f"/sessions/{SESSION_ID}/nodes/{node_id}/answer",
                            json={"user_input": ans})
        except Exception as e:
            print(f"  ❌ API 错误: {e}")
            return False
        can_advance = resp.get("can_advance", False)
        current_round = resp.get("current_round", 0)
        eval_reason = resp.get("evaluation", {}).get("reason", "") if resp.get("evaluation") else ""
        print(f"     → round={current_round}, can_advance={can_advance}")
        if eval_reason:
            print(f"     评估: {eval_reason[:150]}")

        if can_advance:
            print(f"  ✅ [{layer}] 层通关!")
            print(f"  ⏳ 等待异步 LLM 分析(6s)...")
            time.sleep(6)
            return True

    print(f"  ⚠️ [{layer}] 层在 {len(all_answers)} 轮内未通关")
    return False


def run_node(node_id: str, full: bool = True) -> bool:
    """跑通一个节点
    full=True: 完整通关(how/why/system + final_answer)
    full=False: 只跑 how/why,停在 system 层
    """
    answers = ANSWERS[node_id]
    print(f"\n{'='*60}")
    print(f"🎯 进入节点 {node_id}")
    print(f"{'='*60}")

    # enter node
    resp = call_api("POST", f"/sessions/{SESSION_ID}/nodes/{node_id}/enter")
    layer = resp.get("current_layer", "how")
    print(f"  → enter: current_layer={layer}")

    # how 层
    if not run_layer(node_id, "how", answers["how"]):
        print(f"  ❌ {node_id} how 层失败")
        return False

    # why 层
    if not run_layer(node_id, "why", answers["why"]):
        print(f"  ❌ {node_id} why 层失败")
        return False

    if not full:
        print(f"\n  ⏸️  {node_id} 停在 system 层(用户自己走)")
        return True

    # system 层
    if not run_layer(node_id, "system", answers["system"]):
        print(f"  ❌ {node_id} system 层失败")
        return False

    # final_answer
    print(f"\n  📝 提交终问回答...")
    final_ans = answers["final"]
    for attempt in range(3):
        resp = call_api("POST", f"/sessions/{SESSION_ID}/nodes/{node_id}/final-answer",
                        json={"user_input": final_ans})
        verdict = resp.get("verdict", "")
        print(f"  → verdict={verdict}")
        if verdict == "correct":
            print(f"  ✅ {node_id} 终问通过!")
            break
        else:
            print(f"  ⚠️ verdict={verdict},重试...")
            time.sleep(2)
    else:
        print(f"  ⚠️ {node_id} 终问未获 correct,但节点已完成,继续下一个")

    return True


