def make_layer_record(node_id: str, layer: str, answers: list[str]) -> dict:
    """构造一层的完整记录"""
    info = NODE_INFO[node_id]
    ai_q1 = LAYER_QUESTIONS[layer].format(name=info["name"], scope=info["scope"])
    ai_followup = LAYER_FOLLOWUPS[layer]

    # 确保至少3轮回答
    while len(answers) < 3:
        answers = list(answers) + [GENERIC_ROUND3]

    dialogue = [{"role": "ai", "content": ai_q1}]
    for i, ans in enumerate(answers[:3]):
        dialogue.append({"role": "user", "content": ans})
        if i < 2:
            dialogue.append({"role": "ai", "content": ai_followup})

    return {
        "dialogue": dialogue,
        "compressed_summary": "",
        "signals": {"abstraction": 1, "transfer": 1, "example": 1, "compression": 1},
        "score": 8,
        "summary": "本层累积学习行为：抽象1次 迁移1次 举例1次 压缩1次，得分8/8",
        "completed": True,
        "analysis": {"status": "failed"},
    }


def make_completed_node(node_id: str) -> dict:
    """构造一个已完成节点的 node_history entry"""
    answers = ANSWERS[node_id]
    layer_summaries = {}
    layer_records = {}

    for layer in ["how", "why", "system"]:
        rec = make_layer_record(node_id, layer, answers[layer])
        layer_records[layer] = rec
        layer_summaries[layer] = rec["summary"]

    return {
        "node_id": node_id,
        "completed_layers": ["how", "why", "system"],
        "layer_summaries": layer_summaries,
        "layer_records": layer_records,
        "node_completed": True,
        "final_question_completed": True,
        "final_question_verdict": "correct",
    }


